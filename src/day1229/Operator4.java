package day1229;
/**
관계 연산자
*/
public class Operator4{
	public static void main(String[] args) {

		int i=10, k=10, j=12;
		//==은 값이 같다면 true, 값이 다르다면 false

		System.out.println(i+"=="+k+"="+(i==k));
		System.out.println(i+"=="+j+"="+(i==j));

		//!=은 값이 같다면 false, 값이 다르다면 ture

		System.out.println(i+"!="+k+"="+(i!=k));
		System.out.println(i+"!="+j+"="+(i!=j));
	}
}
