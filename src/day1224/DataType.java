package day1224;
/*
	DataType:값을 저장하기 위해 이름을 부여하여 미리 예약해놓은 공간
	기본형 데이터형(Primitive Type)
*/
public class  DataType{
	public static void main(String[] args) {
		byte a=127;//Singed Value:음수와 양수의 범위로 할당

		System.out.println(a);

		//평생 벌고 싶은 돈의 액수를 저장하여 출력하는 코드를 작성하시오.
		long money=475647673L;

		System.out.println(money);

	}//main
}//class
